    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("NIT");
        modelo.addColumn("Telefono");
        modelo.addColumn("DPI");
        modelo.addColumn("Estdo");
        ClienteDAO proveedorDAO = new ClienteDAO();
        
        List<Cliente> proveedor = proveedorDAO.select();
        TablaProducto1.setModel(modelo);
        String[] dato = new String[6];
        for (int i = 0; i < proveedor.size(); i++) {
            dato[0] = Integer.toString(proveedor.get(i).getID());
            dato[1] = proveedor.get(i).getNombre_cliente();
            dato[2] = proveedor.get(i).getNit_cliente();
            dato[3] = proveedor.get(i).getTelefono_cliente();
            dato[4] = proveedor.get(i).getDpi_cliente();
            dato[5] = proveedor.get(i).getEstatus();
            
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }
    
